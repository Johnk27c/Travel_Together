    private void armarCabecera() {
        ArrayList<Object> filaCabecera = new ArrayList<>();
        filaCabecera.add("Nombre");
        filaCabecera.add("Direcci칩n");
        filaCabecera.add("Capacidad");
        filaCabecera.add("Habitaciones");
        filaCabecera.add("Ba침os");
        filaCabecera.add("Precio");
        for (Object it : filaCabecera) {
            modelo.addColumn(it);
        }
        jTable_alojamientos.setModel(modelo);
    }

    private void borrarfilaTabla() {
        int indice = modelo.getRowCount() - 1;

        for (int i = indice; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }
    
    public void cargarCbxAlojamientos() {
        for (Alojamiento alojamiento : alojamientos) {
            jCbx_alojamientos.addItem(alojamiento);
        }
    }
    
    public void cargarCbxPension() {
        for (Pension pension : pensiones) {
            jCbx_pension.addItem(pension);
        }
    }
    

